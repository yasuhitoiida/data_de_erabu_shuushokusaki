    <b>AHPとは</b>
    <p>
      AHP（階層分析法）は複雑な状況での意思決定を行うための構造化法の1つである。AHPは個人的な決定問題で大いに役立つが、複雑な決定問題に取り組む集団意思決定場面で大きな効果を発揮する。例えば利害関係がある場合、人間の認知や判断に頼らざるを得ない場合、さらにはその結果が長期にわたり影響を与える場合に大きな成果をあげている。
